package com.aeviih.core.面向对象;

public class 权限修饰符 {
    public static void main(String[] args){
        // 四种,范围由小到大(private<空着不写<protected<public)
        // 同一个类:private,空着不写,protected,public
        // 同一个类中其他类:空着不写,protected,public
        // 不同包下的子类:protected,public
        // 不同包下的无关类:public
    }
}

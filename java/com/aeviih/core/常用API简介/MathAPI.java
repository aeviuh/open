package com.aeviih.core.常用API简介;

public class MathAPI {
    public static void main(String[] args){
        System.out.println(Math.abs(-1));// 绝对值
        System.out.println(Math.ceil(1.1));// 向上取整
        System.out.println(Math.floor(1.1));// 向下取整
        System.out.println(Math.max(1,2));// 获取两个数的最大值
        System.out.println(Math.pow(2,10));// 返还a的b次幂
        System.out.println(Math.sqrt(65536));// 返还a的平方根
        System.out.println(Math.cbrt(27));// 返还a的立方根
        System.out.println(Math.random());// 返还随机值[0.0,1.0)
        // ...
    }
}

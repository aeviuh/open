package com.aeviih.core.basic;

public class 进制 {
    public static void main(String[] args){
        //十进制
        System.out.println(17);

        //八进制0开头
        System.out.println(017);

        //二进制0b开头
        System.out.println(0b10011);

        //十六进制0x开头
        System.out.println(0X1af7);
    }
}
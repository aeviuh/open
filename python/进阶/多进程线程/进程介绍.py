#导入进程包
import multiprocessing

#通过进程类创建进程对象,子进程
进程对象=multiprocessing.Process()
"""
方法process参数说明:

  target:执行的目标任务名,这里指函数名(方法名)
  name:进程名称，默认Process-1,.....
  group:进程组，目前只能使用None
  args:以元组方式给执行任务传参,如果只有一个参数，参数后面必须加逗号,安照参数的顺序传递
  kwargs:以字典方式给执行任务传参,按照参数的名称传递
  daemon:设置进程的守护主进程，必须在start之前设置.
"""

#启动进程
进程对象.start()

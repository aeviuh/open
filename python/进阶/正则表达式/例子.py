import re

test='''张三,手机号码是13812345678
李四,手机号码是13912345678
王五,手机号码是15012345678
赵六,手机号码是15112345678'''

_re = re.compile(r'(^.*),.*(\d{11}$)',re.M)
for re_ in _re.findall(test):
    print(re_)

    a,b=re_
    print(a)
    print(b)

# re.M 多行匹配模式
# (^.*),.*(\d{11}$)
# ^ 匹配字符串的开头
# .* 匹配任意字符（除了换行符）0次或多次
# , 匹配逗号
# .* 匹配任意字符（除了换行符）0次或多次
# (\d{11}$) 匹配11位数字，并且以数字结尾
# $ 匹配字符串的结尾

# 由于分组,所以输出的是元组